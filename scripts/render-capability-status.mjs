import { createHash } from 'node:crypto';
import { readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const scriptPath = fileURLToPath(import.meta.url);
export const packageRoot = path.resolve(path.dirname(scriptPath), '..');
export const defaultBaselinePath = path.join(packageRoot, 'registry', 'stage0-baseline.json');
export const defaultStatusPath = path.join(packageRoot, 'docs', 'design', 'VIBETETHER-CONTROL-CAPABILITY-STATUS.md');
export const generatedMarker = '<!-- generated by scripts/render-capability-status.mjs; do not edit -->';

function sha256(value) {
  return createHash('sha256').update(value).digest('hex');
}

function cell(value) {
  return String(value ?? '').replaceAll('|', '\\|').replaceAll('\n', ' ');
}

function codeList(values) {
  return values.map((value) => `\`${value}\``).join(', ');
}

function remainingBoundary(feature) {
  if (feature.remaining_boundary) return feature.remaining_boundary;
  if (feature.maturity === 'implemented') return 'Retain installed-package and exact-byte non-regression evidence.';
  if (feature.maturity === 'partial-advisory') return 'Close only the bounded Stage 0 advisory gap; do not imply host enforcement.';
  return `Not implemented; promotion belongs to Stage ${feature.owning_stage}.`;
}

export function renderCapabilityStatus(baseline, baselineBytes) {
  const lines = [
    generatedMarker,
    '# VibeTether Control Capability Status',
    '',
    `- Canonical manifest: \`registry/stage0-baseline.json\``,
    `- Baseline ref: \`${baseline.baseline_ref}\``,
    `- Manifest SHA-256: \`${sha256(baselineBytes)}\``,
    '- This is a deterministic projection, not release authorization.',
    '',
    '## Status vocabulary',
    '',
    '- **implemented** — a current public/runtime path exists; exact package and delivery evidence remain separate gates.',
    '- **partial-advisory** — useful mechanics exist, but an explicit enforcement or coverage boundary remains.',
    '- **designed** — written design exists; Stage 0 does not claim product implementation.',
    '',
    '## Control capability map',
    '',
    '| Capability | Maturity | Current source/test evidence | Remaining boundary | Owning stage |',
    '| --- | --- | --- | --- | ---: |',
  ];
  for (const feature of baseline.control_features) {
    lines.push(`| ${cell(feature.label)} | ${cell(feature.maturity)} | ${cell(codeList(feature.evidence ?? []))} | ${cell(remainingBoundary(feature))} | ${feature.owning_stage} |`);
  }
  lines.push(
    '',
    '## Public capability inventory',
    '',
    '| Capability ID | Maturity | Installed/public journeys |',
    '| --- | --- | --- |',
  );
  for (const capability of baseline.public_capabilities) {
    lines.push(`| \`${cell(capability.id)}\` | ${cell(capability.maturity)} | ${cell(codeList(capability.journeys))} |`);
  }
  lines.push(
    '',
    '## Honest enforcement boundary',
    '',
    'VibeTether-controlled CLI routes may enforce Stage 0 Outcome and evidence gates. An advisory host can still bypass the CLI until Stage 4 Host Enforcement exists.',
    '',
    'Decision Memory, Correction, Claim governance, Host Enforcement, Failure Replay, inspect, and the operator cockpit are designed future-stage work, not Stage 0 product behavior.',
    '',
    '## Completion evidence axes',
    '',
    '| Axis | First required boundary |',
    '| --- | --- |',
  );
  for (const axis of baseline.completion_evidence_axes) lines.push(`| \`${cell(axis.id)}\` | \`${cell(axis.required_for)}\` |`);
  lines.push('', 'No source file, test count, configured CI matrix, historical receipt, or prose claim promotes a row by itself.', '');
  return lines.join('\n');
}

export async function expectedCapabilityStatus(baselinePath = defaultBaselinePath) {
  const baselineBytes = await readFile(baselinePath, 'utf8');
  return renderCapabilityStatus(JSON.parse(baselineBytes), baselineBytes);
}

async function main(argv = process.argv.slice(2)) {
  const check = argv.includes('--check');
  const expected = await expectedCapabilityStatus();
  if (check) {
    const actual = await readFile(defaultStatusPath, 'utf8').catch(() => '');
    if (actual !== expected) {
      process.stderr.write('SELF_STATUS_STALE: regenerate with npm run render:capability-status\n');
      process.exitCode = 1;
      return;
    }
    process.stdout.write('Stage 0 capability status is current.\n');
    return;
  }
  await writeFile(defaultStatusPath, expected, 'utf8');
  process.stdout.write(`${path.relative(packageRoot, defaultStatusPath)} regenerated.\n`);
}

if (process.argv[1] && path.resolve(process.argv[1]) === path.resolve(scriptPath)) {
  await main();
}
