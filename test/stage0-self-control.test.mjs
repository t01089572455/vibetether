import assert from 'node:assert/strict';
import { access, readFile } from 'node:fs/promises';
import path from 'node:path';
import { spawnSync } from 'node:child_process';
import test from 'node:test';
import { expectedCapabilityStatus } from '../scripts/render-capability-status.mjs';

const root = path.resolve(import.meta.dirname, '..');

async function json(relative) {
  return JSON.parse(await readFile(path.join(root, relative), 'utf8'));
}

test('Stage 0 baseline inventories every retained and planned public capability', async () => {
  const [baseline, registry] = await Promise.all([
    json('registry/stage0-baseline.json'),
    json('registry/capabilities.json'),
  ]);
  const actual = registry.capabilities.map(({ id }) => id).sort();
  const planned = baseline.public_capabilities.map(({ id }) => id).sort();
  const retained = baseline.capability_classification.retained_public.toSorted();
  const candidates = baseline.capability_classification.stage0_candidate_public.toSorted();
  assert.equal(actual.length, baseline.candidate_capability_count);
  assert.equal(planned.length, baseline.candidate_capability_count);
  assert.equal(retained.length, baseline.baseline_capability_count);
  assert.deepEqual(actual, planned);
  assert.deepEqual([...retained, ...candidates].sort(), planned);
  assert.deepEqual(baseline.acceptance_ids, [
    'acceptance_ui_golden_screen',
    'acceptance_ui_functional',
    'acceptance_ui_visual',
  ]);
});

